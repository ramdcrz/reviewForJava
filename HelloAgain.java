public class HelloAgain {
    public static void main(String[] args) {
        System.out.println("Hello, world!");
        // This line prints out Hello, world! normally.

        System.out.print("Java\tlanguage\tis\tawesome!");
        /*
        This line prints out
        Java language is awesome
        without the use of any spaces.
        */

        System.out.print("\n\t\tProgramming\t\tis\tfun!");
        /*
        This line prints out
        Programming is fun
        into a new line without using print line
        and also spaces.
        */
    }
}